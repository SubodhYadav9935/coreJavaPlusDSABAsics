package Chapter4.video4_2;

public class Bitwise {
    public static void main(String[] args) {
        // bitwise & , | , ^ it is applied for integers as well for boolean
        // &  both the argument true
        System.out.println(2 & 5);
        // |  at one  argument true
        System.out.println(2 | 5);
        // ^ true if both argument is opposite
        System.out.println(2 ^ 5);
        // bitwise compliment  and not applicable for boolean
        System.out.println(~3);

    }
}
